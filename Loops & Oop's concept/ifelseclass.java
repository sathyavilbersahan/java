public class ifelseclass
{
    public static void main(String[] args)
    {
        int row1=10;
        int row2=12;
        int row3=14;
        if (row1 < row2)
        {
            System.out.println("hello");
        }
        else if(row3 > row2)
        {
            System.out.println("hi");
        }
        else
        {
            System.out.println("bye");
        }
    }
}