public class whileloop {
    public static void main(String[] args) {
        int row = 0;
        int column = 5;
        while (row < 100) {
            System.out.println(row);
            row = row + 2;
        }
        while (column < 100) {
            System.out.println(column);
            column = column + 2;
        }

    }
}
